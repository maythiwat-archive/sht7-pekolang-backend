export function encode(str) {
  return str
    .toUpperCase()
    .split(' ')
    .map(
      word => word
        .split('')
        .map(char => ENCODE_TABLE[char])
        .join(' ')
    )
    .join(' / ')
}

export function decode(str) {
  return str
    .split(' / ')
    .map(
      word => word
        .split(' ')
        .map(char => DECODE_TABLE[char])
        .join('')
    )
    .join(' ')
    .toUpperCase()
}
